# Common Sequence (Both Mutable & Immutable) Operations

even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]

# MIN/MAX
print(min(even))
print(max(even))
print(min(odd))
print(max(odd))

# LEN
print(len(even))
print(len(odd))

print(len("mississippi"))

# COUNT
print("mississippi".count("s"))
print("mississippi".count("iss"))
