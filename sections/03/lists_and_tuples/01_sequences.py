# A sequence is an ordered collection of items.
# In Python, anything that you can iterate over is an iterable. That means that if you can use it in a for loop,-
# then it's an iterable.
# All sequence types can be iterated over.
# That means all sequence types - strings, lists, etc. - are also iterable types.
# Not all iterables are sequences. F.e. you can use a dictionary in a for loop, but it's not a sequence.
