# If serialized data is going to be useful to different programs and computer systems, then it needs to follow a
# well-defined standard.
# JSON is one such standard (ISO = The International Standards Organization)
# That means any valid JSON that you create can be read by any standards-compliant JSON parser, anywhere in the world.

# JSON is an open standard (no patent issues) format for saving and interchanging data.
# JSON is human-readable, as numbers, and other objects, are serialized to plain text. Applications can include a JSON
# parser that takes the JSON text, and parses it into a format that the application can use.
# Python includes a json module.
