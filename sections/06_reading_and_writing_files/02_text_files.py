# A text file holds text.
# A good definition is that a text file is "human-readable".

# Storing data in text files is very convenient, because text files are easy to edit.
# Binary file formats are much harder to work with (video files, audio files, ...)
# Text files can be used to store plain text. Text files can also be used to store data. There are many ways to store
# data in text files, including:
# JSON - a data format derived from Javascript.
# XML (and similar formats such as SSML).
# CSV - Comma Separated Values.
# Pickle files - a Python-specific way to serialize data.
