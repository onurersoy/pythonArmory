# Binary files are files that can contain everything that's stored on your computer's disk drives.
# That also includes text files. Everything that your computer stores - whether that's in memory, on disk, or in the
# cloud - is stored as binary.
#  We differentiate between text files and binary files because many program languages treat text files as a special
#  case and allow us to manipulate them differently. Put simply, f.e. Python automatically decodes the bytes data that
# it reads from the files, and splits it up at the line breaks. It does that for us, for our convenience. That can give
# the impression that text files are somehow stored as text. But as we've seen, text is stored as numbers.
# Text is stored inside a computer in binary. Computers work in zeros and ones – on or off – so everything's binary.
