# newLine
splitString = "This string has been\nsplit over\nseveral\nlines"
print(splitString)

# tab character
tabbedString = "1\t2\t3\t4\t5"
print(tabbedString)

print('The pet shop owner said "No, no, \'e\'s uh,...he\'s resting".')
# or
print("The pet shop owner said \"No, no, 'e's uh,...he's resting\".")
# If we use triple s, there is no need to use an escape character:
print("""The pet shop owner said "No, no, 'e's uh,...he's resting". """)

anotherSplitString = """This string has been
split over
several
lines"""
print(anotherSplitString)

# no newLine
anotherSplitStringNoNewLine = """This string has been \
split over \
several \
lines"""
print(anotherSplitStringNoNewLine)

